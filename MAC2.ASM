*CLICK AN AUDIT COUNTER
*DESTROYS A0
AUDIT	$MACRO	P1
	MOVI	:P1:,A0	
	CALLA	AUD1
	$END
*
*AUDIT A WORD LONG RAM LOCATION
*P1 = AUDIT
*P2 = RAM LOCATION
*DESTROYS A0,A1
AUDITCNT $MACRO	P1,P2
	MOVE	@:P2:,A1,W
	MOVI	:P1:,A0
	CALLA	AUD
	$END
*
*GET A GAME ADJUSTMENT VALUE
*RETURNS ADJUSTMENT VALUE IN A0
ADJUST	$MACRO	P1
	MOVI	:P1:,A0
	CALLA	GET_ADJ
	$END
