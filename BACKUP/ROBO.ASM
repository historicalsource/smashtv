	MOVE	A11,A8
	CALLA	GETANIXY
	ADDI	0300000H,A2
	MOVE	*A0(OFLAGS),A4,W
	CALLA	GANISAG
	MOVE	A0,A9
	
RG	SLEEP	1
	JRUC	RTP
OFFRNG
;MOVE PLAYER WITH RINGS AWAY FROM MUTOID BOSS ENEMY
;A0=PLAYER RING IMAGE
