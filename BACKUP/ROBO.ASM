	CALLA	GETFPAL			;GET A COLOR MAP ASSIGNMENT
	MOVE	A0,*A8(OPAL),W		;STORE THE NEW PALETTE
RTP
	MOVE	@HALT,A0
	JRNE	RG
	MOVE	*A13(TIME),A0
	DEC	A0
	MOVE	A0,*A13(TIME)
	JRZ	KRNGS
	CMPI	100,A0
	JRNE	NYTB
	MOVE	A10,A10
	JRNE	F1
	CREATE	B1PID,BLINK		;BLINK OUT RING PROCESS
