	MOVE	*A0(OPLINK),A0,L

	MOVE	@MUTPTR,A3,L
	MOVE	*A3(PDATA),A3,L		;TNKL
	MOVE	*A3(OXPOS),A2
	MOVE	*A3(OYPOS),A3
	ADDI	20,A2
	ADDI	20,A3
	MOVB	*A0(MYPLYR),A1
	DEC	A1
	SLL	5,A1
	ADDI	PLYRPRCS,A1
	MOVE	*A1,A1,L
	MOVE	*A1(TSO_PTR),A5,L
	MOVE	*A1(LEG2_PTR),A1,L
	
	MOVE	*A1(OXPOS),A4
	MOVE	*A5(OXPOS),A6
	CMP	A4,A2
	JRGT	GONEG
;ADD TO PLYR X
	ADDI	3,A4
	ADDI	3,A6
	JRUC	GOGO
GONEG 
	SUBI	3,A4
	SUBI	3,A6
GOGO	MOVE	A4,*A1(OXPOS)
	MOVE	A6,*A5(OXPOS)
	MOVE	*A1(OYPOS),A4
	MOVE	*A5(OYPOS),A6
	CMPI	182H,A4
	JRLT	GOGO2
	CMP	A4,A3	
	JRGT	GONEG2
;ADD TO PLYR Y
	ADDI	3,A4
	ADDI	3,A6
	JRUC	GOGO1
