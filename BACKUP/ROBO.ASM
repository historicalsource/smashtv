;	MOVX	A3,A2

	MOVE	@STATUS,A3,W	;A2 = [ERROR CODE:STATUS]
	MOVX	A3,A2
				
	MOVE	*SP(0C0H),A3,L	;A3=GET THE RETURN ADDRESS
