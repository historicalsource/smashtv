	MOVI	INTIO,B6
	EXGPC	B6
	MMFM	SP,B0,B2,B6,B10
	RETS

*INITIALIZE I/O
*USE REGISTER CALL FOR TEST MODE NON-RAM USE
INTIO
	MOVI	HESYNC,B2,L
	MOVI	INITDATA,B0,L
	MOVI	(IDATAEND-INITDATA)/16,B10
INITIOS:
	MOVE	*B0+,*B2+
	DSJS	B10,INITIOS
	EXGPC	B6
