;WILL NOT LAND ON ANYONES HEAD
	MOVE	A10,A0
	ANDI	03,A0
	SLL	5,A0
	ADDI	PZLST,A0
	MOVE	*A0,A14,L
	CALLA	DO_ALL_STUFF
;	CALLA	GPALOBJ			;ALLOCATE A COLOR PALETTE 
;	CALLA	STFOBJ			;STUFF OBJECT DATA 
;	MOVE	A13,*A0(OPLINK),L
;	CALLA	INSOBJ			;INSERT OBJECT INTO LIST
	MOVE	A0,A8
	MOVE	A10,*A8(OYPOS),W
	MOVE	A11,*A8(OXPOS),W
	CALLA	ADJSTWTL

WAIT0	SLEEP	5*54		;ABOUT 3 1/2 SECONDS ON SCREEN!
;BASED ON LEVEL?
;BLINK
;A8=ICON IMAGE PTR
	MOVB	@PZFLG,A1		;ICON PICKED UP ALREADY!
	JRNZ	OBJN
	MOVE	@HALT,A0
	JRNZ	WAIT0

	MOVK	7,A10
;ABOUT 1 SECOND OF BLINK
BKZ
	MOVE	A8,A0
	CALLA	OBJOFF
	SLEEP	5
	MOVE	A8,A0
	CALLA	OBJON
	MOVB	@PZFLG,A1		;ICON PICKED UP ALREADY!
	JRNZ	OBJN
	SLEEP	5
	DSJS	A10,BKZ
	SLEEP	30
HHZ:
	MOVB	@PZFLG,A1		;ICON PICKED UP ALREADY!
	JRNZ	OBJN
	MOVE	A8,A0
	CALLA	DELOBJ			;ICONS NOW GONE FOREVER!
OBJN:
	CLR	A0			;RESET FLAG
	MOVB	A0,@PZFLG
IPZ	SLEEP	130	;130
	MOVE	@WAVE,A0
	DEC	A0
	SLL	4,A0
	MOVE	@CIRCUIT,A1
	JRZ	CWV1
	CMPI	1,A1
	JRZ	CWV2
;CIRCUIT 3 CASH
	ADDI	CASHTBL3,A0
	JRUC	CWVO
CWV2	
	ADDI	CASHTBL2,A0
	JRUC	CWVO
CWV1
	ADDI	CASHTBL,A0
CWVO
	MOVE	*A0,A0
	JRZ	PZLUP		;DON'T WANT ANY IN THIS ARENA!
	CALLA	RANDPER
	JRNC	PZLUP
	CLR	A1
	NOT	A1
	MOVI	CLSNEUT|TYPICON|SUBCSH,A0
	CALLA	EXISTOBJ
	JRNZ	PZLUP
;NO CASH ON SCREEN!  LETS GET SOME.
;CHECK TO SEE IF ICONPID IS STILL AROUND.
;IF NOT, THEN PLYRS ARE LEAVING ARENA
	MOVI	ICONPID,A0
	CALLA	EXISTP
	JRZ	PZLUP		;BR=ICON PROC NO LONGER AROUND!

	MOVE	@GAMSTATE,A0
	CMPI	INAMODE,A0
	JRZ	PZLUP

	CREATE	CSHPID,SEND_CSH
	JRUC	PZLUP

;CASH PILE PERCENTAGE FOR CIRCUIT 1 WAVES.
CASHTBL		.WORD	900,900,900,590,1,990,590,0,0,400,0,0
CASHTBL2	.WORD	0,0,400,190,100,0,0,100,200,0,0,0
		.WORD	400,0,0,100,0,0,0
CASHTBL3	.WORD	990,0,0,100,100,0,0,100,100,0,900,0
		.WORD	100,0,0,100,0,0,0,0,0,0
	 

;DO CASH ON GROUND
SEND_CSH
	MOVE	@HALT,A0
	JRNE	CIZZ

	SLEEP	60

	CLR	A1
	NOT	A1
	MOVI	CLSNEUT|TYPICON|SUBCSH,A0
	CALLA	KILOBJ

;GET RANDOM VALID SCRN X, VALID SCRN Y,
;THEN ADD RANDOM OFFSET X/Y FROM THAT X/Y SCRN POS
;PLACE 10-20 IMAGES INTO VALID AREA
;PLACE IMAGES DOWN SLOWLY/SPRINKLE THEM!
;ALLOW THEM TO SCROLL ON WITH A NEW ARENA SCREEN
;DIE
	MOVI	50,B0
	MOVI	260,B1
	CALLA	RANGRAND
	MOVE	A0,A10		;A10=UPLFT X OF AREA
	MOVI	65,B0
	MOVI	170,B1
	CALLA	RANGRAND
	MOVE	A0,A11		;A11=UPLFT Y OF AREA

	CLR	A0
	MOVB	A0,@TMP

	MOVK	15,B0
	MOVK	25,B1
	CALLA	RANGRAND
	MOVE	A0,A9		;A9=# OF CASH ITEMS TO LAY DOWN
	CMPI	19,A0
	JRNC	LPQ
	ANDI	03,A0
	INC	A0
	MOVB	A0,@TMP
LPQ
	MOVI	0,B0
	MOVI	75,B1
	CALLA	RANGRAND
	MMTM	SP,A10,A11
	ADD	A0,A10

	MOVI	0,B0
	MOVI	50,B1
	CALLA	RANGRAND
	ADD	A0,A11
	MOVB	@TMP,A1
	JRZ	LPQ1
;SINGLES TO BE DISPERSED!
	MOVE	A1,A0
	SLL	5,A0
	ADDI	CZ2LST,A0
	JRUC	LPQ2
LPQ1	ANDI	03,A0
	SLL	5,A0
	ADDI	CZLST,A0
LPQ2	MOVE	*A0,A14,L
	CALLA	DO_ALL_STUFF
;	CALLA	GPALOBJ			;ALLOCATE A COLOR PALETTE 
;	CALLA	STFOBJ			;STUFF OBJECT DATA 
;	MOVE	A13,*A0(OPLINK),L
;	CALLA	INSOBJ			;INSERT OBJECT INTO LIST
	MOVE	A0,A8
	MOVE	A10,*A8(OXPOS),W
	MOVE	A11,*A8(OYPOS),W
	CALLA	ADJSTWTL
	MMFM	SP,A10,A11

	SLEEP	3
	DEC	A9
	JRNZ	LPQ
CIZZ	DIE

