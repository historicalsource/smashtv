; SHAKER - SHAKE SCREEN AS AS IF AN EARTHQUAKE IS IN EFFECT
; A10 = # TICKS TO SHAKE
; NOTE: TIME TO SHAKE MUST BE MORE THAN 6 TICKS
; RETURNS:								 
; 	NOTHING AT ALL

SHAKER:
	MOVE	A10,A11
	MOVK	6,A0
	MOVB	A0,@SHK_ON
	DIVS	A0,A11
	MOVB	A0,@AMP
	MOVB	A11,@AMPCNT
	
	CREATE	0,SHAKELP
	RETS
	
SHAKELP:
	CLR	B0
	MOVB	@AMP,B1
	CALLA	RANGRAND
	MOVB	A0,@XSET
	CLR	B0
	MOVB	@AMP,B1
	CALLA	RANGRAND
	MOVB	A0,@YSET
	MOVI	500,A0
	CALLA	RANDPER
	JRC	SHAKEY
SHAKEX:
	MOVB	@XSET,A1
	MOVE	@WORLDTLX,A0,L
	SLL	16,A1
	ADD	A1,A0
	MOVE	A0,@WORLDTLX,L
	SLEEP	1
	MOVB	@XSET,A1
	MOVE	@WORLDTLX,A0,L
	SLL	16,A1
	SUB	A1,A0
	MOVE	A0,@WORLDTLX,L
	JRUC	SHAKELP
SHAKEY:
	MOVB	@YSET,A1
	MOVE	@WORLDTLY,A0,L
	SLL	16,A1
	ADD	A1,A0
	MOVE	A0,@WORLDTLY,L
	SLEEP	1
	MOVB	@YSET,A1
	MOVE	@WORLDTLY,A0,L
	SLL	16,A1
	SUB	A1,A0
	MOVE	A0,@WORLDTLY,L
	MOVB	@AMPCNT,A2
