SEND_PZ
	SLEEP	4*60
	CLR	A0
	MOVB	A0,@PZFLG
PZLUP:
	MOVE	@HALT,A0
	JRNE	IPZ
	MOVB	@PCNT,A8
	ANDI	03,A8
	SLL	4,A8
	MOVB	@BOUND,A0
	JRZ	PZ0
	MOVI	32,A8
;	MOVK	2,A8
;	SLL	4,A8
PZ0
	MOVI	PY,A1
	ADD	A8,A1
	MOVE	*A1,A1,W
	MOVE	A1,B0
	MOVI	PY2,A1
	ADD	A8,A1
	MOVE	*A1,A1,W
	MOVE	A1,B1
	CALLA	RANGRAND
	MOVE	A0,A11

	MOVI	PX,A1
	ADD	A8,A1
	MOVE	*A1,A1,W
	MOVE	A1,B0
	MOVI	PX2,A1
	ADD	A8,A1
	MOVE	*A1,A1,W
	MOVE	A1,B1

	CALLA	RANGRAND
	MOVE	A0,A10
	MOVE	@PLYROBJS,A0,L
	JRZ	AA
	CALLR	CK_HDS
	JRC	NOKA
AA	MOVE	@PLYROBJS+32,A0,L
	JRZ	OKA
	CALLR	CK_HDS
	JRNC	OKA

NOKA	SLEEP	2
	JRUC	PZLUP

PZLST	.LONG	PZ1,PZ2,PZ3,PZ3
